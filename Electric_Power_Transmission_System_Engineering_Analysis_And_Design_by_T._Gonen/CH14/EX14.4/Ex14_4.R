Z_pu_T2 = (1i)*Z_pu*(kVA_B/kVA_Bg2)*(kV_B2/kV_BL_L3)^(2) ;
V1 = kV_B1/kV_BL_L1 ;
V2 = V1 - Ipu * (Z_pu_T1) ;
V4 = V2 - Ipu * (Z_pu_T2) ;
