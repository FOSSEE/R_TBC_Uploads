N3 = 24 ;
N4 = 12 ;
kVA_B = 2080 ;
n1 = N2/N1 ;
n2 = N3/N4 ;
kV_BL_L1 = 2.5 ;
kV_BL_L2 = kV_BL_L1/n1 ;
kV_BL_L3 = kV_BL_L2/n2 ;
Z_B1 = (kV_BL_L1)^(2) * 1000/(kVA_B) ;
Z_B2 = (kV_BL_L2)^(2) * 1000/(kVA_B) ;
Z_B3 = (kV_BL_L3)^(2) * 1000/(kVA_B) ;
