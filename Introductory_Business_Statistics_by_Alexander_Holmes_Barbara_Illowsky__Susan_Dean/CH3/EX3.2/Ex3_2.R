library(MASS)
S=c(1:6)
T=c(2)
A=c(2,4,6)
B=c(1,2,3)
P_T=length(T)/length(S)
cat("a. T =",T)
print(paste("a. P(T) =",fractions(P_T)))
P_A=length(A)/length(S)
cat("b. A =",A)
print(paste("b. P(A) =",fractions(P_A)))
P_B=length(B)/length(S)
cat("c. B =",B)
print(paste("c. P(B) =",fractions(P_B)))
Acomp=setdiff(S,A)
cat("d. A' =",Acomp)
P_Acomp=length(Acomp)/length(S)
print(paste("d. P(A') =",fractions(P_Acomp)))
ABint=intersect(A,B)
ABunion=union(A,B)
P_AintB=length(ABint)/length(S)
P_A_B=P_AintB/P_B
P_B_A=P_AintB/P_A
print(paste("e.P(A|B) =",fractions(P_A_B)))
print(paste("f.P(B|A) =",fractions(P_B_A)))
cat("g. AintB =",ABint)
print(paste("g. P(AintB) =",fractions(P_AintB)))
P_AUB=length(ABunion)/length(S)
cat("h. AUB =",ABunion)
print(paste("h. P(AUB) =",fractions(P_AUB)))
Bcomp=setdiff(S,B)
ABunion=union(A,Bcomp)
cat("i. AUB'=",ABunion)
PABunion=length(ABunion)/length(S)
print(paste("i. P(AUB')=",fractions(PABunion)))
N=c(2,3,5)
PN=length(N)/length(S)
cat("j. N=",N)
print(paste("j. P(N)=",fractions(PN)))
